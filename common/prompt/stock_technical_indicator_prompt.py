# 1.指标类别及具体指标如下：
## 1.1 移动平均线 (Moving Averages):
   ### close_50_sma (50日简单移动平均线): 中期趋势指标。用途：识别趋势方向，并作为动态支撑/阻力位。提示：该指标具有滞后性，建议结合更灵敏的指标以获取及时信号。
   ### close_200_sma (200日简单移动平均线): 长期趋势基准。用途：确认整体市场趋势，识别"金叉/死叉"形态。提示：反应较慢，更适合战略性趋势确认而非频繁的交易入场。
   ### close_10_ema (10日指数移动平均线): 灵敏的短期均线。用途：捕捉动能的快速变化和潜在入场点。提示：在震荡市场中易产生噪音，应结合长周期均线进行信号过滤。

## 1.2 MACD 相关 (MACD Related):
   ### macd (MACD线): 通过指数移动平均线（EMA）的差值计算动能。用途：寻找交叉和背离信号，以此预示趋势转变。提示：在低波动或横盘市场中需配合其他指标确认。
   ### macds (MACD信号线): MACD线的EMA平滑线。用途：利用与MACD线的交叉来触发交易。提示：应作为整体策略的一部分，以避免虚假信号。
   ### macdh (MACD柱状图): 显示MACD线与信号线之间的差距。用途：直观展示动能强度并及早发现背离。提示：波动性可能较大，在快速波动的市场中需补充额外过滤工具。

## 1.3 动能指标 (Momentum Indicators):
   ### rsi (相对强弱指数): 衡量动能，标注超买或超卖状态。用途：应用70/30阈值，观察背离信号以预判反转。提示：在强趋势中，RSI可能长时间处于极端水平，务必结合趋势分析进行交叉验证。

## 1.4 波动率指标 (Volatility Indicators):
   ### boll (布林线中轨): 即20日简单移动平均线，作为布林带的基础。用途：作为价格波动的动态基准。提示：结合上下轨使用可有效识别突破或反转。
   ### boll_ub (布林线上轨): 通常在中轨之上2个标准差处。用途：预示潜在的超买条件和突破区域。提示：需结合其他工具确认信号，强趋势下价格可能沿轨运行。
   ### boll_lb (布林线下轨): 通常在中轨之下2个标准差处。用途：预示潜在的超卖条件。提示：配合其他分析以避免虚假的探底反转信号。
   ### atr (平均真实波幅): 衡量波动率的平均真实范围。用途：根据当前市场波动设置止损水平和调整仓位大小。提示：属于滞后性测量，应作为整体风险管理策略的一部分。

## 1.5 成交量指标 (Volume-Based Indicators):
   ### vwma (成交量加权移动平均线): 结合成交量权重的移动平均线。用途：通过整合价格行为与成交量数据来确认趋势。提示：警惕成交量激增导致的偏离结果，建议结合其他成交量分析工具。

# 2. 任务要求：
   ## 2.1 选择能提供多样化且互补信息的指标，避免冗余（例如：不要同时选择 rsi 和 stochrsi）。
   ## 2.2 简要说明为什么这些指标适合当前的市场背景。
   ## 2.3 撰写一份非常详尽且细致的趋势观察报告。不要简单地描述"趋势不明"，而应提供深入的、颗粒度细化的分析见解，以帮助交易者做出决策。
   ## 2.4 重要提示：当前日期为 {current_date}，我们要分析的公司是 {company_name}。
   
# 3.以下是东方财富专业的指标数据：
{technical_data}
